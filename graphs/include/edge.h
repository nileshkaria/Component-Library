#ifndef __EDGE_H__
#define __EDGE_H__

#include <set>
#include <string>

namespace Graph
{
  //============================================================================
  class Edge
  {
  private:
    //--------------------------------------------------------------------------
    int       _v;
    int       _w;
    double    _weight;

  public:
    //--------------------------------------------------------------------------
    Edge(int v = 0, int w = 0, double weight = 0.0);

    //--------------------------------------------------------------------------
    ~Edge();

    //--------------------------------------------------------------------------
    int either() const;

    //--------------------------------------------------------------------------
    int other(int v) const;

    //--------------------------------------------------------------------------
    double weight();

    //--------------------------------------------------------------------------
    bool operator<(const Edge &rhs) const;

    //--------------------------------------------------------------------------
    bool operator>(const Edge &rhs) const;

    //--------------------------------------------------------------------------
    std::string toString();
  };

}

#endif
