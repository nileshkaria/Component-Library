#ifndef __DIRECTED_EDGE_H__
#define __DIRECTED_EDGE_H__

#include <set>
#include <string>

namespace Graph
{
  //============================================================================
  class DirectedEdge
  {
  private:
    //--------------------------------------------------------------------------
    int       _v;
    int       _w;
    double    _weight;

  public:
    //--------------------------------------------------------------------------
    DirectedEdge(int v = 0, int w = 0, double weight = 0.0);

    //--------------------------------------------------------------------------
    ~DirectedEdge();

    //--------------------------------------------------------------------------
    int from() const;

    //--------------------------------------------------------------------------
    int to() const;

    //--------------------------------------------------------------------------
    double weight();

    //--------------------------------------------------------------------------
    bool operator<(const DirectedEdge &rhs) const;

    //--------------------------------------------------------------------------
    bool operator>(const DirectedEdge &rhs) const;

    //--------------------------------------------------------------------------
    std::string toString();
  };

}

#endif
